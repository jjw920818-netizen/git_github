// ============================================================
//  함수 (Function)
// ============================================================

// -------------------------------------------------------
// 1. 함수 정의 (Definition)
//    function 함수이름(매개변수1, 매개변수2) { ... }
//    → function add(...) { ... } 블록 전체가 "등록" , 실행 X
//    → add(1, 2) 처럼 호출해야 비로소 실행
//    → return은 실행을 시작하는 게 아니라,
//      실행된 함수 안에서 값을 돌려주는 역할
// -------------------------------------------------------

function add(num1, num2) {
    // return : 함수를 호출한 곳으로 값을 되돌려주는 키워드
    //          return을 만나는 순간 함수 실행이 종료
    return num1 + num2;
}

// -------------------------------------------------------
// 2. 함수 호출 (Call) & 반환값 (Return Value)
//    함수이름(인자1, 인자2)  ← 괄호()가 있어야 실행
// -------------------------------------------------------

// add(1, 2) → 함수 실행 → 1 + 2 계산 → 3 반환
// 반환된 3이 result 변수에 저장
let result = add(1, 2);
console.log(result); // 출력: 3


// -------------------------------------------------------
// 3. 함수 자체 vs 함수 호출 — 핵심 차이!
// -------------------------------------------------------

// [케이스 1] add(1, 2) — 함수를 "실행"
//   → 함수가 실행되고, 반환값 3이 남습니다.
add(1, 2);

// [케이스 2] add — 함수 "자체" (실행 X)
//   → 괄호가 없으면 실행되지 않고, 함수 객체 자체를 가리킴
console.log(add); // 출력: [Function: add]


// -------------------------------------------------------
// 4. 함수를 변수에 담기 (일급 함수)
//    JavaScript에서 함수는 값(Value)처럼 변수에 담을 수 있음
// -------------------------------------------------------

// add 함수 자체를 myFunction 변수에 할당
// 이때 add() 로 쓰면 실행 결과(3)가 담기므로 주의!
const myFunction = add; // add 함수 자체를 복사

// myFunction은 이제 add와 동일한 함수
let result2 = myFunction(10, 20);
console.log(result2); // 출력: 30


// -------------------------------------------------------
// 5. return이 없는 함수 → undefined 반환
//    함수는 반드시 반환값을 갖음
//    명시적으로 return을 쓰지 않으면 자동으로 undefined를 반환
// -------------------------------------------------------

function print(message) {
    console.log(message);
    // 여기서 return을 쓰지 않았지만,
    // 내부적으로는 return undefined; 가 실행
}

const printResult = print("Hello"); // 콘솔에 "Hello" 출력
console.log(printResult);           // 출력: undefined
// → print 함수는 반환값이 없으므로 printResult에 undefined가 저장

// -------------------------------------------------------
// 6. 반환값 정리
//
//   함수 안에 return 값  →  그 값이 반환됨
//   함수 안에 return 없음 →  undefined 반환됨
//
//   add(1, 2)    → 3         (return num1 + num2 가 있으므로)
//   print("Hi")  → undefined (return 이 없으므로)
// -------------------------------------------------------