// ============================================================
//  Timer API
//  일정 시간과 관련된 기능을 제공하는 Web API (브라우저 제공)
//
//  setTimeout  : 일정 시간이 지난 후 함수를 딱 한 번 실행
//  setInterval : 일정 시간마다 함수를 반복 실행
//  clearInterval : setInterval로 시작한 반복을 중단
// ============================================================

// -------------------------------------------------------
// 1. setTimeout — 일정 시간 후 한 번 실행
//    setTimeout(실행할함수, 시간ms)
//    ms = 밀리초  (1000ms = 1초)
//
//    ⚡ 실행 순서
//      - setTimeout 자체는 "즉시" 실행 됨(타이머 등록)
//      - 안에 들어간 화살표 함수는 5초가 지난 그 순간에 실행 됨
// -------------------------------------------------------

setTimeout(() => console.log("5초 경과"), 5000);
//         ↑ 화살표 함수              ↑ 5000ms = 5초 후에 실행

// [실험] setTimeout도 타이머 ID를 반환
//        console.log로 찍어보면 숫자(ID)가 출력 됨
//        주석을 풀고 실행해 볼 것 
// const timeOutReturn = setTimeout(() => { }, 5000);
// console.log(timeOutReturn); // 출력 예: 1  (타이머 ID)


// -------------------------------------------------------
// 2. setInterval — 일정 시간마다 반복 실행
//    setInterval(실행할함수, 시간ms)
//
//    ⚡ 실행 순서
//      - setInterval 자체는 "즉시" 실행 됨 (반복 타이머 등록)
//      - 안에 들어간 화살표 함수는 1초마다 반복 실행 됨
//
//    ⚠️  한 번 시작하면 멈추지 않으면 계속 실행됨
//       멈추려면 clearInterval을 사용해야 함
// -------------------------------------------------------

// setInterval은 "타이머 ID"를 반환
// 이 ID를 저장해 두어야 나중에 clearInterval로 멈출 수 있음
const timerId = setInterval(() => console.log("1초마다 실행"), 1000);
//              ↑ 반환된 타이머 ID를 변수에 저장

// [실험] setInterval이 반환하는 타이머 ID를 직접 확인해보는 코드
//        주석을 풀고 실행하면 숫자(ID)가 출력 됨 
// const intervalReturn = setInterval(() => { }, 1000);
// console.log(intervalReturn); // 출력 예: 2  (타이머 ID)


// -------------------------------------------------------
// 3. clearInterval — 반복 실행 중단
//    clearInterval(타이머ID)
//    → setInterval이 반환한 ID를 전달하면 해당 타이머가 중단 됨
// -------------------------------------------------------

clearInterval(timerId);
// → timerId에 해당하는 반복 타이머를 즉시 중단
//   바로 위에서 등록하고 바로 멈췄으므로 실제로 출력되지 않음


// -------------------------------------------------------
// 4. 핵심 개념 정리
//
//   웹API함수(화살표함수, 시간)
//   ↓                  ↓
//   즉시 실행          조건(시간) 만족 시 실행
//
//   setTimeout(() => console.log("실행!"), 3000)
//   → setTimeout 등록:     지금 즉시
//   → 화살표 함수 실행:    3초 후
//
//   이것이 "비동기 프로그래밍"의 핵심
//   등록은 지금 하고, 실행은 나중에 함
//   기다리는 동안 다른 코드가 먼저 실행될 수 있음
// -------------------------------------------------------

